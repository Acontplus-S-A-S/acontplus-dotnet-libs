namespace Acontplus.Core.Enums;

/// <summary>
/// Time zone enum for scheduling and international applications
/// </summary>
public enum TimeZone
{
    /// <summary>
    /// Coordinated Universal Time
    /// </summary>
    UTC = 0,

    /// <summary>
    /// Eastern Standard Time (UTC-5)
    /// </summary>
    EST = 1,

    /// <summary>
    /// Central Standard Time (UTC-6)
    /// </summary>
    CST = 2,

    /// <summary>
    /// Mountain Standard Time (UTC-7)
    /// </summary>
    MST = 3,

    /// <summary>
    /// Pacific Standard Time (UTC-8)
    /// </summary>
    PST = 4,

    /// <summary>
    /// Greenwich Mean Time (UTC+0)
    /// </summary>
    GMT = 5,

    /// <summary>
    /// Central European Time (UTC+1)
    /// </summary>
    CET = 6,

    /// <summary>
    /// Eastern European Time (UTC+2)
    /// </summary>
    EET = 7,

    /// <summary>
    /// Japan Standard Time (UTC+9)
    /// </summary>
    JST = 8,

    /// <summary>
    /// Australian Eastern Standard Time (UTC+10)
    /// </summary>
    AEST = 9,

    /// <summary>
    /// Ecuador Time (UTC-5)
    /// </summary>
    ECT = 10,

    /// <summary>
    /// Colombia Time (UTC-5)
    /// </summary>
    COT = 11,

    /// <summary>
    /// Peru Time (UTC-5)
    /// </summary>
    PET = 12,

    /// <summary>
    /// Chile Time (UTC-3/UTC-4)
    /// </summary>
    CLT = 13,

    /// <summary>
    /// Argentina Time (UTC-3)
    /// </summary>
    ART = 14,

    /// <summary>
    /// Brazil Time (UTC-3)
    /// </summary>
    BRT = 15
}
