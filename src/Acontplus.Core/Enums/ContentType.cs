namespace Acontplus.Core.Enums;

/// <summary>
/// Content type enum for media and file management applications
/// </summary>
public enum ContentType
{
    /// <summary>
    /// Plain text content
    /// </summary>
    Text = 1,

    /// <summary>
    /// HTML content
    /// </summary>
    Html = 2,

    /// <summary>
    /// JSON content
    /// </summary>
    Json = 3,

    /// <summary>
    /// XML content
    /// </summary>
    Xml = 4,

    /// <summary>
    /// PDF document
    /// </summary>
    Pdf = 5,

    /// <summary>
    /// Image content (generic)
    /// </summary>
    Image = 6,

    /// <summary>
    /// JPEG image
    /// </summary>
    ImageJpeg = 7,

    /// <summary>
    /// PNG image
    /// </summary>
    ImagePng = 8,

    /// <summary>
    /// GIF image
    /// </summary>
    ImageGif = 9,

    /// <summary>
    /// SVG image
    /// </summary>
    ImageSvg = 10,

    /// <summary>
    /// Video content (generic)
    /// </summary>
    Video = 11,

    /// <summary>
    /// MP4 video
    /// </summary>
    VideoMp4 = 12,

    /// <summary>
    /// Audio content (generic)
    /// </summary>
    Audio = 13,

    /// <summary>
    /// MP3 audio
    /// </summary>
    AudioMp3 = 14,

    /// <summary>
    /// Microsoft Word document
    /// </summary>
    WordDocument = 15,

    /// <summary>
    /// Microsoft Excel spreadsheet
    /// </summary>
    ExcelSpreadsheet = 16,

    /// <summary>
    /// Microsoft PowerPoint presentation
    /// </summary>
    PowerPointPresentation = 17,

    /// <summary>
    /// ZIP archive
    /// </summary>
    ZipArchive = 18,

    /// <summary>
    /// RAR archive
    /// </summary>
    RarArchive = 19,

    /// <summary>
    /// Binary content (generic)
    /// </summary>
    Binary = 20
}
